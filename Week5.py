weather = input("What is the weather like? ")

if weather == "storm":
    print("Stay inside!")
elif weather == "rain":
    print("Grab an umbrella!")
elif weather == "clouds":
    print("Bring a jacket!")
else:
    print("Enjoy the sunshine!")